                        <h5>El sueño es el entrenamiento silencioso de los deportistas</h5>
                        <p>
                            Para alcanzar la excelencia en el rendimiento o para mejorar los marcadores de salud, ya sea
                            en el caso de un deportista profesional o aficionado, es conveniente cuidar al milímetro
                            cada detalle y es ahí donde radica la importancia del denominado entrenamiento invisible.
                            "Cuando hablamos de entrenamiento invisible nos referimos a todo aquello que el deportista
                            hace entre una sesión de ejercicio y otra, y es tan importante como el que realizamos en la
                            pista, en el parque o en el gimnasio”, explica el doctor Juan Antonio Corbalán, director de
                            la Unidad de Salud Deportiva de Vithas Internacional y ex jugador del Real Madrid de
                            baloncesto y de la selección española.
                            Y uno de los aspectos fundamentales dentro de ese entrenamiento invisible es el que hace
                            referencia al descanso. "El rendimiento deportivo se basa principalmente en el
                            entrenamiento, pero son importantes otros factores como la recuperación física, la
                            preparación psicológica, la nutrición y, por supuesto, el descanso. El sueño es el
                            entrenamiento silencioso de los deportistas", explica por su parte el doctor Eduard
                            Estivill, responsable de las unidades del sueño del Hospital Quirónsalud Vallès y Hospital
                            Universitari General de Catalunya.
                            Y es que un sueño adecuado juega un papel fundamental en el rendimiento, en la recuperación
                            física, fisiológica y metabólica, y en el estado cognitivo y anímico debido a sus efectos
                            reparadores. "Los viajes, los cambios de horario, la comida, el estrés psicosocial, el uso
                            de dispositivos electrónicos, los horarios de los eventos deportivos, las situaciones de
                            ansiedad y el cansancio después de cada evento ponen al deportista al límite de su
                            resistencia, durmiendo pocas horas, en espacios desconocidos y, seguramente, sin poder
                            descansar adecuadamente debido al agotamiento físico", advierte el especialista.
                            Por este motivo, a los deportistas se les debe animar a dormir más horas que al resto de la
                            población para recuperarse del ejercicio y tener una muy buena calidad de sueño. “Los
                            deportistas requieren un sueño adicional para permitir la adaptación a los factores de
                            estrés provocados por el entrenamiento, y minimizar cualquier fatiga residual por el
                            entrenamiento y la competición”, sugiere el doctor Estivill. <br> <br>
                        <h3>El esfuerzo no garantiza un mejor descanso</h3><br><br>
                        <p>
                            Además, contrariamente a lo que se cree, un gran esfuerzo físico “no significa dormir
                            mejor”. “La falta de descanso supone un desgaste añadido que afecta a la recuperación, el
                            estado emocional y el rendimiento. La gestión del sueño de los deportistas y sus equipos, es
                            crucial y diferencial de cara a conservar el máximo rendimiento y mantener el descanso
                            suficiente para la demanda fisiológica durante la prueba deportiva”, señala el experto.
                            Por ello no es nada descabellado acudir a un profesional para someterse a estudios de sueño,
                            de ritmos circadianos y de estado neurocognitivo con el objetivo de realizar un plan de
                            sueño, adaptación a las pautas y horarios, y seguimiento del mismo por parte de los
                            deportistas. La finalidad no es otra que conseguir un mejor rendimiento deportivo tanto en
                            deportistas profesionales como amateurs.

